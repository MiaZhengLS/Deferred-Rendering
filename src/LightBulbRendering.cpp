#include "LightBulbRendering.h"
#include "SDLGraphicsProgram.h"

LightBulbRendering::LightBulbRendering(/* args */)
{
}

void LightBulbRendering::createShaderProgram(std::string vsFile, std::string fsFile)
{
  std::string errorMsg;
  std::string vertexShader = readFileContent(vsFile, errorMsg);
  std::string fragmentShader = readFileContent(fsFile, errorMsg);
  m_shader.createShaderProgram(vertexShader, fragmentShader);
  configureCube();
}

void LightBulbRendering::render(glm::mat4 perspectiveMatrix, std::vector<glm::vec3> lightPositions, std::vector<glm::vec3> lightColors)
{
  m_shader.bind();
  glm::mat4 viewMatrix = SDLGraphicsProgram::instance().getSceneManager().getCamera().getWorldToViewmatrix();

  for (unsigned int i = 0; i < lightPositions.size(); i++)
  {
    glm::mat4 model = glm::mat4(1.0f);
    model = glm::translate(model, lightPositions[i]);
    model = glm::scale(model, glm::vec3(0.125f));
    glm::mat4 mvpMatrix = perspectiveMatrix * viewMatrix * model;
    m_shader.setUniformMatrix4fv("mvpMatrix", &mvpMatrix[0][0]);
    m_shader.setUniform3fv("lightColor", lightColors[i]);
    renderCube();
  }
}

